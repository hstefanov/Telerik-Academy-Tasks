using System;

class FloatingPointVariables
{
    static void Main()
    {
        double firstValue = 34.567839023;
        float secondValue = 12.345f;
        float thirdValue = 3456.091f;
        double fourthValue = 8923.1234857;
        Console.WriteLine("Entered values : {0} {1} {2} {3}", firstValue, secondValue, thirdValue, fourthValue);
    }
}

